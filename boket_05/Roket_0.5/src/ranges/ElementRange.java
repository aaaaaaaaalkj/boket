	/**
	 * 
	 * @return the index of ElementRange for these two cards
	 */
	private static int code(Card card1, Card card2) {
		int c1 = card1.ordinal() - 1; // card-ordinals are 1-based
		int c2 = card2.ordinal() - 1;
		int max = Math.max(c1, c2);
		int min = Math.min(c1, c2);
		return (max * max - max) / 2 + min;
	}

	@SuppressWarnings("null")
	public static final List<@NonNull ElementRange> VALUES = Collections
			.unmodifiableList(Arrays.asList(values()));

	private static final Map<Card, EnumSet<ElementRange>> MAP2 = new HashMap<>();
	static {
		for (Card c1 : Card.getAllCards()) {
			MAP2.put(c1, find(c1.getRank(), c1.getSuit()));
		}
	}

	public static EnumSet<ElementRange> find(final Rank r1, final Suit s1) {
		@SuppressWarnings("null")
		@NonNull
		EnumSet<ElementRange> set = EnumSet.noneOf(ElementRange.class);

		for (Rank r2 : Rank.VALUES) {
			for (Suit s2 : Suit.VALUES) {
				if (r1 != r2 && s1 != s2) {
					set.add(find(r1, s1, r2, s2));
				}
			}
		}
		return set;
	}

	public static ElementRange find(final Rank r1, final Suit s1,
			final Rank r2, final Suit s2) {
		int index = code(Card.instance(r1, s1), Card.instance(r2, s2));
		return VALUES.get(index);
	}

	public static EnumSet<ElementRange> find(final Card c) {
		return MAP2.get(c);
	}

	public static EnumSet<ElementRange> find(final ElementRange e) {
		EnumSet<ElementRange> set1 = MAP2.get(Card.instance(e.r1, e.s1));
		EnumSet<ElementRange> set2 = MAP2.get(Card.instance(e.r2, e.s2));
		set1.addAll(set2);
		return set1;
	}

